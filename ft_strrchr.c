char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*result;

	result = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			result = (char *)&s[i];
		i++;
	}
	return (result);
}
