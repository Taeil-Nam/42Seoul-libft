char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*result;

	result = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (result = (char *)&s[i]);
		i++;
	}
	return (result);
}
